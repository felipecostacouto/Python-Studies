def greet():
    print("Hello")
    print("Bib Bop Boob")
    print("I'm a Python code\n")


greet()


def greet_with_name(name):
    print(f"Hello {name}")
    print("Bib Bop\n")


greet_with_name("Felipe")


def greet_with_name_location(name, location):
    print(f"Hello {name}")
    print(f"What's it like to live on {location} ?\n")


greet_with_name_location("Felipe", "Hollow Earth")



